public class hola {
   private int h;
    private double j;
    private String d;
     private int s;

    public hola() {
    }

    public hola(int h, double j, String d, int s) {
        this.h = h;
        this.j = j;
        this.d = d;
        this.s = s;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public double getJ() {
        return j;
    }

    public void setJ(double j) {
        this.j = j;
    }

    public String getD() {
        return d;
    }

    public void setD(String d) {
        this.d = d;
    }

    public int getS() {
        return s;
    }

    public void setS(int s) {
        this.s = s;
    }

    @Override
    public String toString() {
        return "hola{" + "h=" + h + ", j=" + j + ", d=" + d + ", s=" + s + '}';
    }
     
}
